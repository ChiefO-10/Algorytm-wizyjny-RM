/* USER CODE BEGIN 4 */
void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart){

    if (huart->Instance == USART6)
    {
    	if(rx_index==0){for(i=0;i<10;i++) rx_comand[i]=0;}

    	rx_comand[rx_index] = rx_buff[0];
    	rx_index++;
   	  	if (rx_buff[0]=='e'){
   	  		for(int n=0;n<10;n++){
				if (rx_comand[n]=='L'){
					flag_L_R='L';
					memset(L_motor,0,sizeof(L_motor));
					L_elem=0;
				}
				else if (rx_comand[n]=='R'){
					flag_L_R='R';
					memset(R_motor,0,sizeof(R_motor));
					R_elem=0;
				}
				else if(rx_comand[n]=='e'){
					flag_L_R='e';
				}
				if(flag_L_R == 'L' && rx_comand[n] != 'L'){
					L_motor[L_elem] = rx_comand[n];
					L_elem++;
				}
				else if(flag_L_R == 'R' && rx_comand[n] != 'R'){
					R_motor[R_elem] = rx_comand[n];
					R_elem++;
				}
				else if(flag_L_R == 'e'){

					if(L_motor[0]=='-')L_motor_at=atoi(L_motor);
					else L_motor_at=atoi(L_motor);

					if(R_motor[0]=='-')R_motor_at=atoi(R_motor);
					else R_motor_at=atoi(R_motor);

					Control(L_motor_at,R_motor_at,initper);
					memset(L_motor,0,sizeof(L_motor));
					L_elem=0;
					memset(R_motor,0,sizeof(R_motor));
					R_elem=0;
					rx_index=0;

					flag_L_R=0;

					//char data[18];
					//uint16_t size;
					//static uint8_t cnt;
					//++cnt;
					//size = sprintf(data, "pulse na L %d.\n\r", LMot_Pulse);
					//HAL_UART_Transmit(&huart6, data, size,10);
				}
   	  		}
   	  	}

    }

    HAL_UART_Receive_IT(&huart6, rx_buff, 1);
}
