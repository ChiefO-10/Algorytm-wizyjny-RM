void Control(int PWMLeft, int PWMRight, int init_period){

	uint8_t L_dir=4, R_dir=4;       // direction of motor rotation L298N

	if(PWMLeft > 0){
		LMot_Pulse = PWMLeft;
		L_dir = 0;
	}
	else if(PWMLeft < 0){
		LMot_Pulse = abs(PWMLeft);
		L_dir = 1;
	}
	else if(PWMLeft == 0){
			LMot_Pulse = 0;
			L_dir = 2;
	}

	if(PWMRight > 0){
		RMot_Pulse = PWMRight;
		R_dir = 0;
	}
	else if(PWMRight < 0){
		RMot_Pulse = abs(PWMRight);
		R_dir = 1;
	}
	else if(PWMRight == 0){
			RMot_Pulse = 0;
			R_dir = 2;
	}

	if(L_dir == 0) {		// set direction of left motor rotation
		HAL_GPIO_WritePin(GPIOF,GPIO_PIN_12,GPIO_PIN_SET);
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_15,GPIO_PIN_RESET);
	}
	else if (L_dir == 1) {
		HAL_GPIO_WritePin(GPIOF,GPIO_PIN_12,GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_15,GPIO_PIN_SET);
	}
	else if (L_dir == 2) {
		HAL_GPIO_WritePin(GPIOF,GPIO_PIN_12,GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_15,GPIO_PIN_RESET);
	}

	if(R_dir == 0) {		// set direction of left motor rotation
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_14,GPIO_PIN_SET);
		HAL_GPIO_WritePin(GPIOA,GPIO_PIN_7,GPIO_PIN_RESET);
	}
	else if (R_dir == 1) {
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_14,GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOA,GPIO_PIN_7,GPIO_PIN_SET);
	}
	else if (R_dir == 2) {
		HAL_GPIO_WritePin(GPIOD,GPIO_PIN_14,GPIO_PIN_RESET);
		HAL_GPIO_WritePin(GPIOA,GPIO_PIN_7,GPIO_PIN_RESET);
	}
	TIM1->CCR1 = (init_period * LMot_Pulse)/100u;
	TIM1->CCR2 = (init_period * RMot_Pulse)/100u;

}
